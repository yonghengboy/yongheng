package homework.review.chapter01;
//普通for循环遍历输出一维数组元素
public class TraverseOutput {
    public static void main(String[] args) {
        //定义一维数组
        int[] arr ={1,2,34,5,6,7};
        //for输出arr
        for(int i = 0;i<arr.length;i++){
            System.out.println(arr[i]);
        }
    }
}
